#include "libft.h"

size_t		ft_nblen(long n)
{
	size_t	size;

	size = 1;
	if (n < 0)
	{
		n = n * -1;
		size++;
	}
	while (n >= 10)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
